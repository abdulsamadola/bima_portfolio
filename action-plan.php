 <p>
                                <strong>SOCIAL INTERVENTION PROGRAMMES:</strong> Niger South Senatorial district is not only more populated than other zones of the state, but it also harbors industrious, hardworking, influential and educated people. Nupeland is blessed with natural and human resources. The administration will ensure the participation and enlightenment of everyone from the district through:
                                   <br>
                                <ul class="custom-bullet-list">
                               <li>Women and youth empowerment through skill acquisition programmes, starter packs and soft loans</li>
                               <li>Giving out of scholarships to the talented children from poor family backgrounds</li>
                               <li>Prompt assistance to those affected by flood, fire and other related disasters.</li>
                               <li>Provision of social amenities like portable water supply, electricity and health care delivery.</li>                              
                           </ul>

