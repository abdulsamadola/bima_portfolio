 <p>
                                <strong>ARTS AND CRAFTS:</strong> The industrial base of the Nupes was equally enormous as local industries abound in the past. Cloth weaving, glass making, brass and silver smiting were among the indigenous industries that had existed in  Bida, Kutigi, Doko, Mokwa, Enagi, Danchitagi and some other parts of Nupeland since the early 15th century. Others were canoe building by the people living along the riverine areas. Hats and mats weaving commonly found at Kutigi, Enagi and Kudu were highly acknowledged by foreign tourists. Arts, crafts and Culture were very exciting, elevating and redolent of our trademark in the past. Those fortunes of Nupeland were life enhancing, entertaining, which defined our identities in the national and international spheres. All those wonderful fortunes of Nupe kingdom dwindled after the colonial incursion into our territory. To redeem the past glories of the land, the following will be undertaken:
                                    <br>
                                <ul class="custom-bullet-list">
                               <li>Revival Mission team will be constituted to visit the centres and villages known for these ancestral occupations to educate them on the importance of the traditional crafts and empowerment will be reimbursed for them.</li>
                               <li>Necessary support will be given to the centres that are still actively involved in their traditional trades. E.g. glass making at Masag, Brass smiting at Dokodza, Silver smiting at Tswata Mukun Bida, and Mat/Hat weaving centre at Efu Ndakada Kutigi, Kudu and Enagi.</li>
                               <li>These centres will be assisted to get loans from banks at low interest rate to let them shift from local to modern method of production</li>
                               <li>Traditional institutions will be involved in awareness campaign as traditional leaders still command respect from their subjects.</li>                              
                           </ul>

                                </p>

