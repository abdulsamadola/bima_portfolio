<br>
    Muhammad Bima Enagi was born on the 7th September 1959. He received his early education at Enagi Primary School from 1967 to 1973. This disciplined Prince from the Bima Royal family was at the prestigious Kings College Lagos, where he obtained his WAEC Certificate with Grade 1 Distinction in 1978. His urge for more knowledge took him to the School of Basic Studies, Ahmadu Bello University, Zaria, where he acquired his IJMB certificate in 1979. He was in the main stream of the university in 1979 where he bagged his B.Sc degree with the second class upper division in Quantity Surveying in 1982.
<br>
 His one year mandatory service (NYSC) was at Owah Unik Consultants, Warri from 1982 to 1983. Based on his outstanding professional qualities, Muhammad Bima was offered an appointment as the Project officer at Owah Unik Consultants, Warri immediately after his NYSC programme, and he worked there for four years (1983 – 1987). He moved to First Bank Nigeria’s Head Office in Lagos as the Project Officer in 1987, where as a result of his high professional expertise and enviable character was appointed as the Project Coordinator and Secretary, Head Office Tenders Communication, a position he held from 1988 till 1990. He is a gentleman that stands tall among his professional colleagues, and in the year 1990, he was offered appointment at the Central Bank of Nigeria as an Assistant Project Manager.
<br>
     Muhammad Bima is a believer of reasonable academic orientation. He proceeded to the University of Lagos, Akoka in 1991 where he obtained MBA Degree in Business Administration in 1992. He also holds a Post Graduate Diploma Degree in Computer Science from the Federal University of Technology, Minna. Being a dedicated and tireless officer that works very well even under pressure, he received due promotions at the Central Bank of Nigeria. He was promoted to the post of Project Manager in 1994, Senior Project Manager in 2001, Assistant Director in 2006, Deputy Director in 2011 and Director in 2018 just before his retirement.
<br>
     This registered member of Nigeria Institute of Quantity Surveyors who touched the lives of many people positively attended series of professional workshops, seminars, trainings and conferences within and outside the country. He is a good family man, married with children.
